import Foundation
import Hue

extension UIColor {
    static func coffeeBrown() -> UIColor {
        return UIColor(hex: "3B3A35")
    }
    
    static func coffeeCreme() -> UIColor {
        return UIColor(hex: "ECDFBD")
    }
    
    static func coffeeBlue() -> UIColor {
        return UIColor(hex: "31507f")
    }
    
    static func coffeeBlueNav() -> UIColor {
        return UIColor(hex: "20457C")
    }
    
    static func coffeeOrange() -> UIColor {
        return UIColor(hex: "FB6648")
    }
    
    static func coffeePurple() -> UIColor {
        return UIColor(hex: "5E3448")
    }
}
